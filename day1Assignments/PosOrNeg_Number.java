package week1.day1Assignments;

public class PosOrNeg_Number {

	public static void main(String[] args) {
		int num = 78;
		if(num>0)
		{	
		System.out.println("The given number " +num+" is Possitive Number");	
		}
		else if(num==0)
		{
		System.out.println("The given number " +num+" is Neutral Number");	
		}
		else
		{
		System.out.println("The given number " +num+ " is Negative number");
		}


	}

}
